    private int extractNumber(String maBan) {
        try {
            return Integer.parseInt(maBan.replaceAll("\\D", ""));
        } catch (NumberFormatException e) {
            e.printStackTrace();
            return -1; // Chỉ thị lỗi hoặc số không hợp lệ
        }
    }


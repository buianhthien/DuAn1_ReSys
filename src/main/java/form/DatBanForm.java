    private void update() {
        JFrame mainFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        int rowCount = tblDanhSachGoiMon.getRowCount();

        if (rowCount > 0) {
            try {
                for (int row = 0; row < rowCount; row++) {
                    int maHDCT = (int) tblDanhSachGoiMon.getValueAt(row, 0);
                    Object value = tblDanhSachGoiMon.getValueAt(row, 3);
                    int soLuong = 0;
                    if (value instanceof Integer) {
                        soLuong = (Integer) value;
                    } else if (value instanceof String) {
                        soLuong = Integer.parseInt((String) value);
                    } else if (value instanceof Double) {
                        soLuong = ((Double) value).intValue();
                    }
                    System.out.print(soLuong);
                    String ghiChu = (String) tblDanhSachGoiMon.getValueAt(row, 5);
                    HoaDonChiTiet hdct = hdctdao.selectByma(maHDCT);
                    hdct.setSoLuong(soLuong);
                    hdct.setGhiChu(ghiChu);
                    hdctdao.update(hdct);
                }
                this.fillTableHDCT();
                this.tongTien();
                this.updateTableButtons();
                MsgBox.alert(mainFrame, "Cập nhật thông tin hóa đơn chi tiết thành công!");
            } catch (Exception e) {
                e.printStackTrace();
                MsgBox.alert(mainFrame, "Cập nhật thông tin hóa đơn chi tiết thất bại!");
            }
        } else {
            MsgBox.alert(mainFrame, "Chưa có thông tin để cập nhật!");
        }
    }

    private void deleteHDCT() {
        JFrame mainFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        if (tblDanhSachGoiMon.getRowCount() > 0) {
            int Row = tblDanhSachGoiMon.getSelectedRow();
            if (Row >= 0) {
                int maHDCT = (int) tblDanhSachGoiMon.getValueAt(Row, 0);
                int maHD = (int) tblDanhSachGoiMon.getValueAt(Row, 1);
                try {
                    hdctdao.delete(String.valueOf(maHDCT));

                    if (hdctdao.countByMaHD(maHD) == 0) {
                        hddao.delete(String.valueOf(maHD));
                        this.updateTableButtons();
                        MsgBox.alert(mainFrame, "Xóa hóa đơn và tất cả chi tiết thành công!");
                    } else {
                        MsgBox.alert(mainFrame, "Xóa hóa đơn chi tiết thành công!");
                    }
                    this.fillTableHDCT();
                    this.tongTien();
                } catch (Exception e) {
                    e.printStackTrace();
                    MsgBox.alert(mainFrame, "Xóa hóa đơn chi tiết thất bại!");
                }
            } else {
                MsgBox.alert(mainFrame, "Chọn một hàng để xóa!");
            }
        } else {
            MsgBox.alert(mainFrame, "Chưa có món để xóa!");
        }

    }

    private double tongTien() {
        double tongTien = 0;
        for (int i = 0; i < tblDanhSachGoiMon.getRowCount(); i++) {
            double tien = (double) tblDanhSachGoiMon.getValueAt(i, 4);
            tongTien += tien;
        }
//        String formattedTongTien = String.format("%,.0f", tongTien) + " VND";
//        lblTongTien.setText(formattedTongTien);
          lblTongTien.setText(DinhDang.formatCurrency(tongTien)+" VND");
        return tongTien;
    }


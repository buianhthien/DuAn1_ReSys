    private boolean checkForm() {
        JFrame mainFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        String maBan = lblMaBan.getText();
        String soLuong = txtSoLuong.getText();
        if (maBan.equals("0")) {
            MsgBox.alert(mainFrame, "Bạn chưa chọn bàn để thêm món!");
            return false;
        } else if (!checkBanExists(maBan)) {
            MsgBox.alert(mainFrame, "Bàn này không tồn tại trong hệ thống.");
            return false;
        } else if (soLuong.isEmpty()) {
            MsgBox.alert(mainFrame, "Số lượng món không được bỏ trống!");
            return false;
        } else {
            return true;
        }
    }


